Map::Map()
{
	src.x = 0;
	src.y = 0;
	src.w = 32;
	src.h = 32;

	dest.x = 0;
	dest.y = 0;
	dest.w = 32;
	dest.h = 32;
	texture = TextureManager::LoadTexture("..\\Ressources\\img\\tile.png");
	loadMap(lvl1);
}

Map::~Map()
{
}

void Map::loadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::drawMap()
{
	int type = 0;
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];
			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::draw(texture, src, dest);
				break;
			case 1:
				TextureManager::draw(texture, src, dest);
			default:
				break;
			}
		}
	}
}
